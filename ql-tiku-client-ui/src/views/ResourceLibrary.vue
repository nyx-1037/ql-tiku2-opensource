    </div>

    <div class="content">
      <!-- Ëá™ÂÆö‰πâË°®Ê†º -->
      <div v-if="fileList.length > 0" class="simple-table">
        <div class="table-header">
          <div class="header-cell">Êñá‰ª∂Âêç</div>
          <div class="header-cell">ÊèèËø∞</div>
          <div class="header-cell">Êñá‰ª∂Â§ßÂ∞è</div>
          <div class="header-cell">‰∏ä‰º†Êó∂Èó¥</div>
          <div class="header-cell">‰∏ãËΩΩÊ¨°Êï∞</div>
          <div class="header-cell">Êìç‰Ωú</div>
        </div>
        <div
          v-for="file in fileList"
          :key="file.id"
          class="table-row"
        >
          <div class="table-cell file-name-cell">
            <div class="file-name">
              <t-icon name="file-text" v-if="isDocument(file.fileExtension)" class="file-icon" />
              <t-icon name="image" v-else-if="isImage(file.fileExtension)" class="file-icon" />
              <t-icon name="folder" v-else class="file-icon" />
              <span>{{ file.originalFileName }}</span>
            </div>
          </div>
          <div class="table-cell">
            <span>{{ file.description || 'ÊöÇÊó†ÊèèËø∞' }}</span>
          </div>
          <div class="table-cell">
            <span>{{ formatFileSize(file.fileSize) }}</span>
          </div>
          <div class="table-cell">
            <span>{{ formatDateTime(file.uploadTime) }}</span>
          </div>
          <div class="table-cell">
            <span>{{ file.downloadCount }}</span>
          </div>
          <div class="table-cell action-cell">
            <div class="action-buttons">
              <t-button
                theme="primary"
                size="small"
                @click="copyUrl(file)"
              >
                Â§çÂà∂ÈìæÊé•
              </t-button>
              <t-button
                theme="success"
                size="small"
                @click="previewFile(file)"
              >
                È¢ÑËßà
              </t-button>
              <t-button
                theme="default"
                size="small"
                @click="downloadFile(file)"
              >
                ‰∏ãËΩΩ
              </t-button>
            </div>
          </div>
        </div>
      </div>

      <!-- Â¶ÇÊûúÊ≤°ÊúâÊï∞ÊçÆÊòæÁ§∫Á©∫Áä∂ÊÄÅ -->
      <div v-if="fileList.length === 0 && !loading" class="empty-state">
        <t-empty description="ÊöÇÊó†Êñá‰ª∂" />
      </div>

      <div class="pagination">
        <CustomPagination
          :current="currentPage"
          :page-size="pageSize"
          :total="total"
          :page-size-options="[
            { value: 10, label: '10' },
