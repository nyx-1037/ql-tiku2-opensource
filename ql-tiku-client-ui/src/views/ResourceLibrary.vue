<script>
import { ref, onMounted, onUnmounted, computed } from 'vue'
import { MessagePlugin } from 'tdesign-vue-next'
import { useResourceLibraryStore } from '@/store/resourceLibrary'
import CustomPagination from '../components/CustomPagination.vue'
import LoadingStates from '../components/LoadingStates.vue'

export default {
  name: 'ResourceLibrary',
  components: {
    CustomPagination,
    LoadingStates
  },
  setup() {
    // ‰ΩøÁî®ÁºìÂ≠òstore
    const resourceStore = useResourceLibraryStore()
    
    const searchKeyword = ref('')

    // ‰ªéstoreËé∑ÂèñÊï∞ÊçÆ
    const fileList = computed(() => resourceStore.resources)
    const total = computed(() => resourceStore.pagination.total)
    const loading = computed(() => resourceStore.loading)

    // Âä†ËΩΩÁä∂ÊÄÅÂíåÈîôËØØÂ§ÑÁêÜ
    const isLoading = computed(() => resourceStore.loading || false)
    const error = computed(() => null) // ÊöÇÊó∂ËÆæ‰∏∫nullÔºåÂõ†‰∏∫store‰∏≠Ê≤°ÊúâerrorÂ±ûÊÄß
    const retryCount = computed(() => 0)
    const maxRetries = computed(() => 3)

    // ÈîôËØØÂ§ÑÁêÜÊñπÊ≥ï
    const handleRetry = () => {
      resourceStore.loadResources()
    }

    const clearError = () => {
      // ÊöÇÊó∂Á©∫ÂÆûÁé∞
    }
    
    // Êú¨Âú∞ÂàÜÈ°µÁä∂ÊÄÅÔºàÁî®‰∫éUIÁªëÂÆöÔºâ
    const currentPage = ref(1)
    const pageSize = ref(12)

    // Ëé∑ÂèñÊñá‰ª∂ÂàóË°® - ‰ΩøÁî®storeÊñπÊ≥ï
    const getFileList = () => {
      const params = {
        keyword: searchKeyword.value.trim() || '',
        page: currentPage.value,
        size: pageSize.value
      }
      
      resourceStore.loadResources(params)
    }

    // ÊêúÁ¥¢
    const handle = () => {
      currentPage.value = 1
      getFileList()
    }

    // ÂàÜÈ°µÂ§ßÂ∞èÊîπÂèò
    const handleSizeChange = (size) => {
      pageSize.value = size
      currentPage.value = 1
      getFileList()
    }

    // ÂΩìÂâçÈ°µÊîπÂèò
    const handleCurrentChange = (page) => {
      currentPage.value = page
      getFileList()
    }

    // Â§çÂà∂ÈìæÊé•
    const copyUrl = async (file) => {
      try {
        await navigator.clipboard.writeText(file.fullUrl)
        MessagePlugin.success('ÈìæÊé•Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø')
      } catch (error) {
        // ÈôçÁ∫ßÊñπÊ°à
        const textArea = document.createElement('textarea')
        textArea.value = file.fullUrl
        document.body.appendChild(textArea)
        textArea.select()
        document.execCommand('copy')
        document.body.removeChild(textArea)
        MessagePlugin.success('ÈìæÊé•Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø')
      }
    }

    // È¢ÑËßàÊñá‰ª∂
    const previewFile = async (file) => {
      try {
        // Â¢ûÂä†‰∏ãËΩΩÊ¨°Êï∞
        await resourceStore.incrementDownloadCount(file.id)
        
        // ÊâìÂºÄÈ¢ÑËßàÈìæÊé•
        window.open(file.fullUrl, '_blank')
        
        // Êõ¥Êñ∞Êú¨Âú∞ÊòæÁ§∫ÁöÑ‰∏ãËΩΩÊ¨°Êï∞
        file.downloadCount = (file.downloadCount || 0) + 1
      } catch (error) {
        console.error('È¢ÑËßàÂ§±Ë¥•:', error)
        MessagePlugin.error('È¢ÑËßàÂ§±Ë¥•')
      }
    }

    // ‰∏ãËΩΩÊñá‰ª∂ - ‰ΩøÁî®fileAPIÁöÑdownloadFileÊñπÊ≥ï
    const downloadFile = async (file) => {
      try {
        MessagePlugin.info('Ê≠£Âú®ÂáÜÂ§á‰∏ãËΩΩ...')
        
        console.log('üöÄ [ResourceLibrary] ÂºÄÂßã‰∏ãËΩΩÊñá‰ª∂:', {
          fileId: file.id,
          fileName: file.originalFileName
        })
        
        // ‰ΩøÁî®fileAPIÁöÑdownloadFileÊñπÊ≥ïÔºåÂÆÉÂ∑≤ÁªèÈÖçÁΩÆ‰∫ÜÊ≠£Á°ÆÁöÑresponseType: 'blob'
        const response = await resourceStore.downloadFileFromServer(file.id)
        
        // Ëé∑ÂèñÊñá‰ª∂ÂêçÔºà‰ªéÂìçÂ∫îÂ§¥Êàñ‰ΩøÁî®ÂéüÂßãÊñá‰ª∂ÂêçÔºâ
        const contentDisposition = response.headers.get('Content-Disposition') || response.headers.get('content-disposition')
        let fileName = file.originalFileName
        if (contentDisposition) {
          const fileNameMatch = contentDisposition.match(/filename[^;=\n]*=((['"]).*?\2|[^;\n]*)/)
          if (fileNameMatch && fileNameMatch[1]) {
            fileName = decodeURIComponent(fileNameMatch[1].replace(/['"]/g, ''))
          }
        }
        
        // Ëé∑ÂèñÊñá‰ª∂blob
        const blob = await response.blob()
        
        // ÂàõÂª∫‰∏ãËΩΩÈìæÊé•
        const url = window.URL.createObjectURL(blob)
        const link = document.createElement('a')
        link.href = url
        link.download = fileName
        link.style.display = 'none'
        
        // Ëß¶Âèë‰∏ãËΩΩ
        document.body.appendChild(link)
        link.click()
        document.body.removeChild(link)
        
        // Ê∏ÖÁêÜURLÂØπË±°
        window.URL.revokeObjectURL(url)
        
        // Êõ¥Êñ∞Êú¨Âú∞ÊòæÁ§∫ÁöÑ‰∏ãËΩΩÊ¨°Êï∞ÔºàÂêéÁ´ØÂ∑≤ÁªèËá™Âä®Â¢ûÂä†‰∫ÜÔºâ
        file.downloadCount = (file.downloadCount || 0) + 1
        
        console.log('‚úÖ [ResourceLibrary] Êñá‰ª∂‰∏ãËΩΩÊàêÂäü:', fileName)
        MessagePlugin.success('Êñá‰ª∂‰∏ãËΩΩÊàêÂäü')
      } catch (error) {
        console.error('‚ùå [ResourceLibrary] ‰∏ãËΩΩÂ§±Ë¥•:', error)
        
        // Ê£ÄÊü•ÊòØÂê¶ÊòØÊñá‰ª∂‰∏çÂ≠òÂú®ÁöÑÈîôËØØ
        if (error.message.includes('404') || error.message.includes('Not Found')) {
          MessagePlugin.error('Êñá‰ª∂‰∏çÂ≠òÂú®ÊàñÂ∑≤Ë¢´Âà†Èô§')
        } else if (error.message.includes('500')) {
          MessagePlugin.error('ÊúçÂä°Âô®ÂÜÖÈÉ®ÈîôËØØÔºåËØ∑Á®çÂêéÈáçËØïÊàñËÅîÁ≥ªÁÆ°ÁêÜÂëò')
        } else {
          MessagePlugin.error(`‰∏ãËΩΩÂ§±Ë¥•: ${error.message}`)
        }
      }
    }

    // Âà§Êñ≠ÊòØÂê¶‰∏∫ÊñáÊ°£Á±ªÂûã
    const isDocument = (extension) => {
      const docTypes = ['.pdf', '.doc', '.docx', '.xls', '.xlsx', '.ppt', '.pptx', '.txt']
      return docTypes.includes(extension?.toLowerCase())
    }

    // Âà§Êñ≠ÊòØÂê¶‰∏∫ÂõæÁâáÁ±ªÂûã
    const isImage = (extension) => {
      const imageTypes = ['.jpg', '.jpeg', '.png', '.gif', '.bmp', '.webp']
      return imageTypes.includes(extension?.toLowerCase())
    }

    // Ê†ºÂºèÂåñÊñá‰ª∂Â§ßÂ∞è
    const formatFileSize = (size) => {
      if (!size) return '0 B'
      const units = ['B', 'KB', 'MB', 'GB']
      let index = 0
      let fileSize = size
      
      while (fileSize >= 1024 && index < units.length - 1) {
        fileSize /= 1024
        index++
      }
      
      return `${fileSize.toFixed(1)} ${units[index]}`
    }

    // Ê†ºÂºèÂåñÊó•ÊúüÊó∂Èó¥
    const formatDateTime = (dateTime) => {
      if (!dateTime) return ''
      const date = new Date(dateTime)
      return date.toLocaleString('zh-CN', {
        year: 'numeric',
        month: '2-digit',
        day: '2-digit',
        hour: '2-digit',
        minute: '2-digit'
      })
    }

    // ÁªÑ‰ª∂ÊåÇËΩΩÊó∂ÂàùÂßãÂåñstore
    onMounted(() => {
      // ÂàùÂßãÂåñÊï∞ÊçÆÔºà‰ºö‰ºòÂÖà‰ΩøÁî®ÁºìÂ≠òÔºâ
      const params = {
        page: currentPage.value,
        size: pageSize.value
      }
      
      if (typeof resourceStore.initializeData === 'function') {
        resourceStore.initializeData(params).catch(err => {
          console.error('ÂàùÂßãÂåñÂ§±Ë¥•Ôºå‰ΩøÁî®ÈôçÁ∫ßÊñπÊ°à:', err)
          // ÈôçÁ∫ßÂà∞Áõ¥Êé•Ë∞ÉÁî®loadResources
          resourceStore.loadResources(params).catch(loadErr => {
            console.error('Âä†ËΩΩËµÑÊ∫êÂ§±Ë¥•:', loadErr)
          })
        })
      } else {
        // Â¶ÇÊûúÊ≤°ÊúâinitializeDataÊñπÊ≥ïÔºåÁõ¥Êé•Ë∞ÉÁî®loadResources
        resourceStore.loadResources(params).catch(err => {
          console.error('Âä†ËΩΩËµÑÊ∫êÂ§±Ë¥•:', err)
        })
      }
    })

    // ÁªÑ‰ª∂Âç∏ËΩΩÊó∂Ê∏ÖÁêÜ
    onUnmounted(() => {
      // ÂèØ‰ª•Âú®ËøôÈáåÊ∑ªÂä†Ê∏ÖÁêÜÈÄªËæëÔºåÂ¶ÇÂèñÊ∂àÊú™ÂÆåÊàêÁöÑËØ∑Ê±Ç
    })

    return {
      loading,
      searchKeyword,
      fileList,
      currentPage,
      pageSize,
      total,
      getFileList,
      handle,
      handleSizeChange,
      handleCurrentChange,
      copyUrl,
      previewFile,
      downloadFile,
      isDocument,
      isImage,
      formatFileSize,
      formatDateTime,
      // Âä†ËΩΩÁä∂ÊÄÅÂíåÈîôËØØÂ§ÑÁêÜ
      isLoading,
      error,
      retryCount,
      maxRetries,
      handleRetry,
      clearError
    }
  }
}
</script>

